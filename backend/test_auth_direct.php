<?php
// /tmp/test_auth_direct.php
$url = 'http://localhost/radio-app/backend/api/auth.php';
$pin = 'Leyla-Milo-1408';

$ch = curl_init($url);
curl_setopt($ch, CURLOPT_RETURNTRANSFER, true);
curl_setopt($ch, CURLOPT_POST, true);
